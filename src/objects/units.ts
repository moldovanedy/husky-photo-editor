/**
 * A 2d point with x and y coordinates
 */
export interface Point {
    x: number;
    y: number;
}
/**
 * This unit represents the amount of an unit applied as x and y numbers. For example, a layer moved 15px on x axis and 35px on y axis can be represented as Vector2(15, 35)
 */
export interface Vector2 {
    x: number;
    y: number;
}
